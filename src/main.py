def greet_user(name: str):
  """ This function greet the user and onboard the user to devops cli tool """
  return f"Hello {name.title()}, Welcome to the devops cli tool"

def full_name(first, last):
  """ Function that will return the user fullname. """
  return f"{first.title()} {last.name()}"

